BOOL CMainFrame::OnCreateClient(LPCREATESTRUCT lpcs, CCreateContext* pContext)
{
	BOOL bRet = m_spw.CreateStatic(this, 1, 2); //分割成两行一列
	CRect rcClient;
	GetClientRect(&rcClient);

	//填充view
	bRet = m_spw.CreateView(0, 0, //位置 - 0行0列
		RUNTIME_CLASS(CTreePaintView),  //填充CTreeView
		SIZE{ rcClient.Width() / 3, rcClient.Height() },
		pContext);

	bRet = m_spw.CreateView(0, 1, //位置 - 1行0列
		RUNTIME_CLASS(CPaintView),
		SIZE{ rcClient.Width() * 2 / 3, rcClient.Height() },
		pContext);
	m_spw.RecalcLayout();
	return bRet;
}


